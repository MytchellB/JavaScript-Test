var i;

function changeBG(newBG) {
	switch(newBG) {
		case "blackBG":
			document.body.style.backgroundColor="black";
			break;
		case "whiteBG":
			document.body.style.backgroundColor="white";
			break;
		case "blueBG":
			document.body.style.backgroundColor="blue";
			break;
	}
}

function blackBG(){
	document.body.style.backgroundColor="black";
}

function whiteBG(){
	document.body.style.backgroundColor="white";
}

function blueBG(){
	document.body.style.backgroundColor="blue";
}